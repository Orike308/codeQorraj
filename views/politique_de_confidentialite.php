                        e-mail, etc</li>
                    <li><strong>Données administratives et financières :</strong> informations issues de votre
                        carte d'assurance maladie, factures, coordonnées bancaires, correspondances, prises de
                        rendez-vous, etc.</li>
                    <li><strong>Données relatives à la santé :</strong> contenu de votre dossier médical,
