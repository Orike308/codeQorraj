            <h3 class="phraseConfidentialite">But du traitement des données</h3>
            <p class="texteConfidentialite">Notre politique est claire et transparente : nous utilisons des données
                personnelles dans le but d’assurer nos services et activités. <br> Nous traitons en premier lieu des
                données personnelles que nous recevons, dans le cadre de nos relations contractuelles avec les
                personnes
                concernées de la part de celles-ci et d’autres personnes impliquées.</p><br> <br>

            <h3 class="phraseConfidentialite">Collecte et traitement des données personnelles</h3>
            <p class="texteConfidentialite">Nous traitons des données personnelles en particulier dans les
                catégories
                suivantes de traitement :</p><br>

            <div class="listeConfidentialite">
                <ul class="listeAPointConfidentialite">
                    <li>En cas de fournitures de prestations</li>
                    <li>En cas de participation à l’un de nos événement</li>
                    <li>Lorsque nous communiquons ou qu’une visite a lieu</li>
                    <li>En cas de candidatures pour un poste</li>
                    <li>Lorsque nous sommes tenus de traiter les données pour des raisons légales ou réglementaires
                    </li>
                    <li>Lorsque nous exerçons notre obligation de diligence ou défendons d’autres intérêts
                        légitimes, p. ex. pour éviter les conflits d’intérêts ou d’autres risques, garantir
                        l’exactitude des données, vérifier la solvabilité, assurer la sécurité ou faire valoir nos
                        droits</li>
