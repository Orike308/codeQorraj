            <p class="texteConfidentialite">Une donnée personnelle désigne toute information liée, directement ou
                indirectement, à une personne identifiable. Cela inclut par exemple votre nom, adresse, numéro de
                téléphone, ou numéro AVS, mais aussi des informations plus sensibles, comme celles contenues dans
                votre
                dossier médical.</p> <br> <br>

