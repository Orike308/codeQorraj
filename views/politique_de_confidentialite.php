                seront traitées par nos soins. Centre Qorraj Sàrl est susceptible de collecter et de traiter les
                types
                de données suivants :</p><br>

            <div class="listeConfidentialite">
                <ul class="listeAPointConfidentialite">
                    <li><strong>Données d'identification :</strong> nom, prénom, date de naissance, numéro ASV,
