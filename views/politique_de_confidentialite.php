                        d'analyses)</li>
                    <li>Lorsque nous conservons un dossier médical et, dans certains cas, déclarons certaines
                        maladies aux autorités compétentes ou transmettons des données à des tiers (assureurs, par
                        exemple)</li>
                    <li>Vos données peuvent être traitées pour améliorer la qualité des services, réaliser des
