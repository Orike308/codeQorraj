            <h3 class="phraseConfidentialite">Durée de conservation des données personnelles</h3>
            <p class="texteConfidentialite">Nous traitons et conservons vos données personnelles aussi longtemps que
                cela est nécessaire pour remplir nos obligations contractuelles et légales ou pour atteindre les
                objectifs poursuivis par le traitement des données, c’est-à-dire, par exemple, pour la durée de la
                relation dans son ensemble (de l’initiation, du déroulement et jusqu’à la fin d’un traitement) ainsi
                qu’au-delà, conformément aux obligations légales de conservation et de documentation. <br> Ce
                faisant,
                il est possible que des données personnelles soient conservées pendant la période au cours de
                laquelle
                des droits peuvent être exercés contre notre entreprise (c.-à-d. notamment pendant la prescription
                légale) et dans la mesure où nous y sommes contraints par la loi ou si des intérêts commerciaux
                légitimes l’exigent (p. ex. à des fins de preuves et de documentation). Dès que vos données
                personnelles
                ne sont plus nécessaires aux objectifs susmentionnés, elles sont en principe et dans la mesure du
                possible effacées ou rendues anonymes.</p><br><br>

